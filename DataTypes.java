public class DataTypes {
    public static void main(String[] args) {
     byte a = 10; // Length of byte is 1.
     short b = 30000; // Length of short is 2 byte.
     int c = 980328; // Length of Integer is 4 byte .
     float m = 23.99f; //Length of float is 4 byte.
     char d = 'r'; //Length of character is 2 byte.
     boolean e = true;
     System.out.println(a);
     System.out.println(b);
     System.out.println(c);
     System.out.println(d);
     System.out.println(m);
     System.out.println(e);

    }
}
